--추가문제. (기본 문제 풀이가 모두 끝난 후 작성한다.)
          HR계정의 EMPLOYEES 테이블에서 커미션 받는 사람의 수와
          안받는 사람의 수를 조회한다.
          출력형태 ---------------
              구분        인원수
          ------------------------
          커미션받는사원    XXX
          커미션없는사원    XXX
          모든사원          XXX
/*
GRANT SELECT ON EMPLOYEES TO SCOTT;
*/
SELECT NVL(T.구분, '전체') "구분", T.인원수 "인원수"
FROM
(
    SELECT CASE WHEN COMMISSION_PCT IS NOT NULL THEN '커미션받는사원'
                WHEN COMMISSION_PCT IS  NULL THEN '커미션없는사원'
    ELSE '0' END "구분"
    , COUNT(*) "인원수"
    FROM HR.EMPLOYEES
    GROUP BY ROLLUP(CASE WHEN COMMISSION_PCT IS NOT NULL THEN '커미션받는사원'
                WHEN COMMISSION_PCT IS  NULL THEN '커미션없는사원'
    ELSE '0' END)
) T;
