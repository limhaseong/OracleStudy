--33. TBL_INSA 테이블에서 BASICPAY + SUDANG 이 
    100만원 미만, 100만원 이상~200만원 미만, 
    200만원 이상인 직원들의 수 조회.
SELECT NVL(TO_CHAR(T2.급여), '전체') "급여"
    , COUNT(*) "인원수"
FROM
(
    SELECT CASE WHEN T1.급여 < 1000000 THEN '1000000미만'
                 WHEN T1.급여 < 2000000 THEN '1000000이상 2000000미만'
                 WHEN T1.급여 >= 2000000 THEN '2000000이상'
            ELSE '0'
            END "급여"
    FROM
    (
           SELECT BASICPAY + SUDANG "급여"
            FROM TBL_INSA
    ) T1
) T2
GROUP BY ROLLUP(T2.급여);
