--94. 부서별 평균 급여를 조회하되, A, B, C 등급으로 나눠서 출력.
    200만원 초과 - A등급
    150~200만원  - B등급
    150만원 미만 - C등급
SELECT F.부서 "부서명"
      , CASE WHEN F.급여평균 > 2000000 THEN 'A등급'
             WHEN F.급여평균 < 2000000 THEN 'B등급'
             WHEN F.급여평균 < 1500000 THEN 'C등급'
             ELSE '알수없음'
        END "부서등급"
FROM
(
    SELECT NVL(TO_CHAR(T.부서), '전체') "부서", COUNT(*) "인원수", SUM(T.급여+T.수당) "급여합", AVG(T.급여+T.수당) "급여평균"
    FROM
    (
        SELECT CASE WHEN SUBSTR(BUSEO, 1,2) IN ('개발') THEN '개발부'
                     WHEN SUBSTR(BUSEO, 1,2) IN ('영업') THEN '영업부'
                     WHEN SUBSTR(BUSEO, 1,2) IN ('총무') THEN '총무부'
                     WHEN SUBSTR(BUSEO, 1,2) IN ('기획') THEN '기획부'
                     WHEN SUBSTR(BUSEO, 1,2) IN ('인사') THEN '인사부'
                     WHEN SUBSTR(BUSEO, 1,2) IN ('자재') THEN '자재부'
                     WHEN SUBSTR(BUSEO, 1,2) IN ('홍보') THEN '홍보부'
                     ELSE '모름'
                END "부서"
                ,BASICPAY "급여" , SUDANG "수당"
        FROM TBL_INSA
    ) T
    GROUP BY T.부서
) F;
